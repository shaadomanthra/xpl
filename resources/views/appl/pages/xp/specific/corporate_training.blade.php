    <h2 >What constitutes an effective training programme?</h2>
    <p class="mb-5">Without a doubt, everyone has their own view of what constitutes a "good training programme." The truth is that there is no one-size-fits-all plan for the optimum training programme. However, some aspects are universal in all successful training programmes, regardless of industry, workforce size, programme aim, geography, or demography.</p>
   

            <div class="row text-md-left text-center">
              <div class="col-sm-6 mb-3 mb-sm-5">
                <!-- Icon Blocks -->
                <div class="pr-lg-6">
                  <h4><i class="fa fa-angle-right"></i> Relevance and Need</h4>
                  <p>The experience and knowledge you provide employees must be current, relevant, and appropriate to their day-to-day tasks. It should assist them in broadening their knowledge and abilities, and it should be simple to understand so that they can learn quickly and put what they've learned into practice. </p>
                 

                  <h4 class="mt-4"><i class="fa fa-angle-right"></i> Alignment</h4>
                  <p>Everything taught to employees must be in line with the company's objectives. Instead of asking employees what training they want or need, it is essential to focus on the business goals and then the competencies required to accomplish them. Then, it can be decided on which skills employees require training on.
                  </p>
                </div>
                <!-- End Icon Blocks -->
              </div>

              <div class="col-sm-6 mb-3 mb-sm-5">
                  <!-- Icon Blocks -->
                <div class="pr-lg-6">
                  <h4 class=""><i class="fa fa-angle-right"></i>Goal-oriented</h4>
                  <p>When designing training programmes, it should be made sure they are goal-oriented and that the Key Performance Indicators chosen offer a comprehensive picture of the program's time, cost, effectiveness, quality, and quantity.</p>
                
                  <h4 class="mt-4"><i class="fa fa-angle-right"></i>Creativity</h4>
                  <p>Workplace learning is changing to the point where adding entertaining and relevant activities to training is becoming more common as a way to boost employee engagement. Moving away from typical trainer-style learning or an online learning experience that resembles flipping through a PowerPoint presentation will allow exploring more creative options.</p>
                
