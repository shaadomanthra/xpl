    <h2 >Types of Corporate Training</h2>
    <p class="mb-5">Depending on their size, diversity, and the characteristics of their industry, different organisations may have a variety of training programmes. The following is a list of typical corporate training programmes:</p>
   

            <div class="row text-md-left text-center">
              <div class="col-sm-6 mb-3 mb-sm-5">
                <!-- Icon Blocks -->
                <div class="pr-lg-6">
                  <h4><i class="fa fa-angle-right"></i> Orientation and onboarding</h4>
                  <p>This type of training begins as soon as a newly hired employee arrives at work and continues until the person is able to operate independently. Its primary goal is to speed up the new employee onboarding process by making them feel more at ease and productive.</p>
                  <p>The onboarding training programme can be customised. However, it usually consists of two parts:
                    <ul>
                      <li>For New Hires: It contains general company information, such as its history, mission, vision, and values, as well as corporate policies and regulations.</li>

                      <li>For a Particular Role: This focuses on acquiring basic information and abilities that a new hire will need to succeed in a particular position.</li>
                    </ul>
                  </p>

                  <h4 class="mt-4"><i class="fa fa-angle-right"></i> Compliance training</h4>
                  <p>Compliance training is frequently included in new hire onboarding and is a necessity. It's usually a structured programme that focuses on corporate regulations or guidelines, allowing employees and employers to avoid both workplace difficulties and legal breaches.</p>
                  <p>These regulations and procedures are usually related to specific employment or sector. What workers should be compliant with and what kind of training is required can also be influenced by location. Anti-harassment and diversity training programmes, company ethics, workplace safety, and data protection and privacy training are all examples of compliance training.
                  </p>
                </div>
                <!-- End Icon Blocks -->
              </div>

              <div class="col-sm-6 mb-3 mb-sm-5">
                  <!-- Icon Blocks -->
                <div class="pr-lg-6">
                  <h4 class=""><i class="fa fa-angle-right"></i>Training in Hard Skills</h4>
                  <p>There's always space for growth, no matter how professional employees are. Employee development is centred on hard skills since this type of training allows employees to strengthen the abilities they require in their jobs and become even better professionals. A graphic designer who has mastered Photoshop, for example, can advance by learning how to work in other design software. At the same time, a programmer who is proficient in CSS and JavaScript can receive additional training to understand Python programming. To stay current with the latest advances, employees must undergo frequent training.</p>
                
                  <h4 class="mt-4"><i class="fa fa-angle-right"></i>Training in Soft Skills</h4>
                  <p>Companies should not disregard soft skill training because how staff behave is just as important as what they know. According to studies, employees can favourably improve the company's profitability and increase ROI by developing soft skills.</p>
                  <p>Communication, problem-solving, and decision-making are examples of soft talents that pertain to interpersonal relationships. They assist employees in interacting with their coworkers and customers in a productive and pleasant manner. They're crucial for attracting new customers, boosting customer service, and forming a solid team.</p>

                  <h4 class="mt-4"><i class="fa fa-angle-right"></i>Training on product knowledge</h4>
                  <p>Product knowledge is the ability of team members to thoroughly comprehend and convincingly speak with customers about what you offer, including its features, benefits, applications, and costs. This training is frequently provided as part of a new hire's onboarding programme. It's also beneficial for employees who need to brush up on the company's products and services, and it's held whenever a new feature or product is released so that everyone is up to date.</p>
                
